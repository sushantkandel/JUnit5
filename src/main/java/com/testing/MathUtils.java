package com.testing;

public class MathUtils {
    public int sum(int a, int b) {
        return a + b;
    }

    public long divide(int a, int b) {
        return a / b;
    }

    public long multiply(long a, long b) {
        return a * b;
    }

    public double subtract(long a, long b) {
        return a - b;
    }
}
